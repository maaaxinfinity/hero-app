// The google-services plugin materializes Firebase config from
// google-services.json (FCM). That file is a credential and is gitignored, so a
// public checkout won't have it — apply the plugin only when it's present. FCM
// then stays inert at runtime (RemotePush falls back to UnifiedPush) rather than
// failing the build. firebase-messaging is still linked (see androidMain) so the
// service compiles regardless.
if (file("google-services.json").exists()) {
    apply(plugin = libs.plugins.googleServices.get().pluginId)
}

// --- Single release version source ---------------------------------------------
// gradle.properties#appVersion / #appVersionCode is the only edit point for a
// release version. Everything below DERIVES from it — Android
// versionName/versionCode, desktop packageVersion, and the runtime AppVersion
// constant (generated Version.kt, see generateVersionKt) — so the four release
// identities can no longer drift apart in-repo. The Release workflow closes the
// remaining gap by asserting the git tag equals v<appVersion> before publishing.
val appVersion: String = providers.gradleProperty("appVersion").get().also {
    require(Regex("""\d+\.\d+\.\d+""").matches(it)) {
        "gradle.properties appVersion must be MAJOR.MINOR.PATCH, got \"$it\""
    }
}
val appVersionCode: Int = providers.gradleProperty("appVersionCode").get().toInt().also {
    require(it > 0) { "gradle.properties appVersionCode must be a positive int" }
}

